async function main() {
  console.log(DRY_RUN
    ? '🔍  DRY RUN — no deletions will happen. Pass --confirm to delete.\n'
    : '⚠️   LIVE MODE — records WILL be deleted.\n'
  );

  await mongoose.connect(process.env.MONGO_URI || process.env.MONGODB_URI);
  console.log('✅  Connected to MongoDB\n');

  // Check which ones actually exist in v2
  const found = await TransactionV2.find({
    transactionId: { $in: BAD_IDS }
  }).lean();

  console.log(`🎯  Found ${found.length} of ${BAD_IDS.length} IDs in TransactionV2\n`);

  if (found.length === 0) {
    console.log('✅  Nothing to delete — already clean.');
    await mongoose.disconnect();
    return;
  }

  // Preview
  for (const t of found) {
    console.log(`  • ${t.transactionId} — ₦${(t.totalAmount || 0).toLocaleString()}`);
  }

  const totalNaira = found.reduce((sum, t) => sum + (t.totalAmount || 0), 0);
  console.log(`\n  Total: ₦${totalNaira.toLocaleString()} across ${found.length} records`);

  if (DRY_RUN) {
    console.log('\n⛔  Dry run complete. Run with --confirm to permanently delete.');
    await mongoose.disconnect();
    return;
  }

  // Delete
  const result = await TransactionV2.deleteMany({
    transactionId: { $in: BAD_IDS }
  });

  console.log(`\n🗑️   Deleted ${result.deletedCount} TransactionV2 records.`);
  console.log('\n✅  Rollback complete. You can now re-run registerMatchedTransactions.js with proper filters.\n');

  await mongoose.disconnect();
}

main().catch(err => {
  console.error('❌  Error:', err.message);
  mongoose.disconnect();
  process.exit(1);
});